#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}

/*
retorno da funcao é o len de src

if size for 0 retorna direto o src

no while é i < size - 1 pq o ultimo valor de size é reservado para
o \0 que tem q ser forçado a ser colocado na ultima posicao
*/